#include "minitalk.h"

void	ft_error(unsigned char error)
{
	if (error & ERROR_0)
		printf("%s", ERR_MS0);
	if (error & ERROR_1)
		printf("%s", ERR_MS1);
	if (error & ERROR_2)
		printf("%s", ERR_MS2);
	if (error & ERROR_3)
		printf("%s", ERR_MS3);
}
