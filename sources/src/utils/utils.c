#include "minitalk.h"

void	ft_print_bits(unsigned char octet)
{
	int i;
	char bits;

	i = 7;
	while (i >= 0)
	{
		bits = ((octet >> i) & 1) + '0';
		ft_putstr_fd(Green"", 1);
		write(1, &bits, 1);
		i--;
	}
}
